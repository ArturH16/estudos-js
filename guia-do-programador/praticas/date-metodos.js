let dia = new Date()
//getDay retorna dia do mês
console.log(dia.getDay())
//getDate retorna o dia da semana (começa com 0 - domingo)
console.log(dia.getDate())
//getFullYear retorna o ano com 4 dígitos
console.log(dia.getFullYear())
//Retorna a hora no momento
console.log(dia.getHours())
//Retorna os minutos no momento
console.log(dia.getMinutes())
//Retorna os milisegundos no momento
console.log(dia.getMilliseconds())
//Retorna o mês (0 - janeiro...)
console.log(dia.getMonth())
//Retorna os segundos no momento
console.log(dia.getSeconds())
//Método getTime retorna o número de milissegundos transcorridos considerando a data base 1 de janeiro de 1970
console.log(dia.getTime())
//Método getTimezoneOffset retorna a diferença entre a hora local e a hora GMT em minutos
console.log(dia.getTimezoneOffset())
